    private void txt_qtyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_qtyActionPerformed
        // qty
        if (txt_qty.getText() != null && !txt_qty.getText().trim().equals("")) {
            double qty = Double.parseDouble(txt_qty.getText());
            double qtyAvb = Double.parseDouble(txt_qtyavb.getText());
            if (qty <= qtyAvb) {
                double uprice = Double.parseDouble(txt_unitprice.getText());
                double total = qty * uprice;
                txt_itemtotal.setText(""+total);
                txt_itemdiscount.grabFocus();
            }else{
                JOptionPane.showMessageDialog(this, "Qty Not Available..!");
                txt_qty.setText(null);
                txt_qty.grabFocus();
            }
        }
    }//GEN-LAST:event_txt_qtyActionPerformed

    private void txt_itemdiscountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_itemdiscountActionPerformed
        // Discount%
        if (txt_itemdiscount.getText() != null && !txt_itemdiscount.getText().trim().equals("")) {
            double total = Double.parseDouble(txt_itemtotal.getText());
            double itemdiscount = Double.parseDouble(txt_itemdiscount.getText());
            double qty = Double.parseDouble(txt_qty.getText());
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            Vector v = new Vector();
            v.add(txt_itemid.getText());
            v.add(txt_itemname.getText());
            v.add(txt_unitprice.getText());
            
            InvoiceItemRow checkRowRepeating = checkRowRepeating(txt_itemid.getText());
            
            if (checkRowRepeating != null) {
                total += checkRowRepeating.getTotal();
                qty += checkRowRepeating.getQty();
            }
            
            
            double subtotal = total * (100 - itemdiscount) / 100;
            txt_subtotal.setText("" + subtotal);
            
            v.add(qty);
            v.add(total);
            v.add(itemdiscount);
            v.add(subtotal);
            dtm.addRow(v);
            
            clearFields();
            calculateTotal();
        }
    }//GEN-LAST:event_txt_itemdiscountActionPerformed

