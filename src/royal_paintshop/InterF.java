    private void generateInvoiceID() {
        try {
            ResultSet rs = DB.search("select count(*) as inv_count from invoice");
            if (rs.next()) {
                int count = rs.getInt("inv_count");
                String invoiceid = "INV"+(++count);
                txt_invoiceid.setText(invoiceid);
            }
            txt_itemid.grabFocus();
        } catch (Exception e) {
        }
    }

